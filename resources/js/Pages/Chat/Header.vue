import { Link } from '@inertiajs/vue3';

const props = defineProps({
    character: {
        type: Object,
        required: true,
        default: () => ({
            name: '',
            tagline: '',
        })
    }
});

const getNameInitial = (name) => {
    if (!name) return '';
    const parts = name.split(' ');
    if (parts.length === 0) return '';
    return parts[0].charAt(0).toUpperCase();
}

